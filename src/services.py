import logging
import aiohttp
from .config import BACKEND_URL

async def send_to_backend(message):
    logging.info(f"[MOCK] would send to backend: {message}")
    async with aiohttp.ClientSession() as session:
        try:
            async with session.post(BACKEND_URL, json=message) as resp:
                if resp.status != 200:
                    logging.warning(f"–û—à–∏–±–∫–∞ –æ—Ç–ø—Ä–∞–≤–∫–∏: {resp.status}")
                    return None
                return await resp.json()  # üëà –≤–µ—Ä–Ω—ë–º –æ—Ç–≤–µ—Ç –æ—Ç –±—ç–∫–∞
        except Exception as e:
            logging.error(f"–û—à–∏–±–∫–∞ —Å–≤—è–∑–∏ —Å backend: {e}")
            return None